package Class2;

public class ArithTask {

    public static void main(String[] args) {

        double dogTreats=50;
        double catTreats=40;
        double addResults=dogTreats+catTreats;
        System.out.println(dogTreats+catTreats);
        System.out.println("The addition of the two numbers of "+dogTreats+ " and " +catTreats+ " is equal to " +addResults);

        double subResults=dogTreats-catTreats;
        System.out.println(dogTreats-catTreats);
        System.out.println("The subtraction of the two numbers of "+dogTreats+ " and " +catTreats+ " is equal to " +subResults);

        double multResults=dogTreats*catTreats;
        System.out.println(dogTreats*catTreats);
        System.out.println("The multiplication of the two numbers of "+dogTreats+ " and " +catTreats+ " is equal to " +multResults);

        double divResults=dogTreats/catTreats;
        System.out.println(dogTreats/catTreats);
        System.out.println("The division of the two numbers of "+dogTreats+ " and " +catTreats+ " is equal to " +divResults);

        double remResults=dogTreats%catTreats;
        System.out.println(dogTreats%catTreats);
        System.out.println("The remainder of the two numbers of "+dogTreats+ " and " +catTreats+ " is equal to " +remResults);

    }


}
