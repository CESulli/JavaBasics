package Class5;

public class E4OROperator {

    public static void main(String[] args) {

        boolean cond1=true;
        boolean cond2=false;
        boolean cond3=false;
        boolean result=cond1||cond2||cond3; //whether cond1 or cond2 is true, it will be true
        System.out.println(result); //we need one condition to be true and the end result is true

    }
}
