package Class6;
//print all the even numbers from 2 to 200
public class T5WhileEven200 {
    public static void main(String[] args) {

        int i=2; //starting point
        while (i <= 200) { //ending point
            System.out.print(i+" "); //adds a space between numbers printed
            i+=2; //steps

        }
    }
}
